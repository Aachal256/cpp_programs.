//WAP to  print size of an int ,float,char type variable.
#include<iostream>
using namespace std;
int main()
{
    int a;
    float b;
    char c;
    cout<<"size of int="<<sizeof(a)<<endl;
    cout<<"size of float="<<sizeof(b)<<endl;
    cout<<"size of char="<<sizeof(c);
    return 0;

}

/*
i/p---  size of int=4
        size of float=4
         size of char=1
*/